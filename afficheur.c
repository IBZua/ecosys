void afficheur(const char message[]) {
  //boucle ligne
  for (int i = 0; i < N_LIGNES ; ++i)
  {
    //boucle lettre
    for (int j = 0; j < strlen(message); ++j)
    {
      //boucle colonne
      for (int k = 0; k < N_COLONNES; ++k)
      {
        if (message[j] == ' ')
        {
          printf("%c", espace[i][k]);
        }else{
          int lettre_index = message[j]-'A';
          printf("%c", lettre[lettre_index][i][k]);
        }
      }
    }
    printf("\n");
  }
  
}